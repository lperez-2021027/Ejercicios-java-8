public class EstructuraDeControlIf {
	public static void main(String args[]) {

		int num = 25;
		int num2 = 2;
		// entrando cloque if (si entonces)
		if (num != num2) {
			// boque de verdad
			System.out.println("El numero es diferente");

		} else {
			// bloque de falso
			System.out.println("el numero es igual :o");
		}
		// fin del bloque if
	}

}