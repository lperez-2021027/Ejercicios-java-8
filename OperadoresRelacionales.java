public class OperadoresRelacionales {
	public static void main(String args[]) {

		int x = 10;
		int y = 0;
		// comparacion si es igual
		System.out.println(x == y);
		// comparando si 10 es igual a 0
		System.out.println(x != y);
		// comparando si 10 no es igual a 0
		System.out.println(!(x != y));
		// negacion del valor

	}

}